# Merkle Root calculation (mirrors lib/security.ts)
# ---------------------------------------------------------------------------
def calculate_merkle_root(logs: list) -> str:
    """Compute SHA-256 Merkle root over agent execution logs."""
    if not logs:
        return hashlib.sha256(b"empty").hexdigest()
    
    leaves = []
    for log in logs:
        data = json.dumps(log, sort_keys=True).encode("utf-8")
        leaves.append(hashlib.sha256(data).hexdigest())
    
    while len(leaves) > 1:
        if len(leaves) % 2 != 0:
            leaves.append(leaves[-1])  # duplicate last leaf if odd
        next_level = []
        for i in range(0, len(leaves), 2):
            combined = (leaves[i] + leaves[i + 1]).encode("utf-8")
            next_level.append(hashlib.sha256(combined).hexdigest())
        leaves = next_level
    
    return leaves[0]

